CREATE OR REPLACE VIEW data_model_sxf.fact_order_shipment
(
    order_id,
    fulfillment_airport_code,
    carrier_service_id,
    tracking_number,
    item_gender,
    item_count,
    estimated_shipped_local_datetime,
    estimated_delivered_local_datetime,
    carrier_delivered_local_datetime,
    actual_weight,
    is_sub_one_pound,
    zone_rate_shopping,
    base_rate,
    total_surcharges,
    net_charge_amount,
    discount,
    shipment_freight_charge_before_discount,
    fuel_surcharge,
    residential_surcharge,
    address_id,
    is_split_order,
    is_out_of_region_order,
    order_type,
    shipment_type,
    carrier_ingested_local_datetime,
    is_scrub,
    package_length,
    package_width,
    package_height,
    package_dim_unit,
    only_shoes,
    meta_create_datetime,
    meta_update_datetime
)
AS
SELECT meta_original_order_id,
       fulfillment_airport_code,
       carrier_service_id,
       tracking_number,
       item_gender,
       item_count,
       estimated_shipped_local_datetime,
       estimated_delivered_local_datetime,
       carrier_delivered_local_datetime,
       actual_weight,
       is_sub_one_pound,
       zone_rate_shopping,
       base_rate,
       total_surcharges,
       net_charge_amount,
       discount,
       shipment_freight_charge_before_discount,
       fuel_surcharge,
       residential_surcharge,
       address_id,
       is_split_order,
       is_out_of_region_order,
       order_type,
       shipment_type,
       carrier_ingested_local_datetime,
       is_scrub,
       package_length,
       package_width,
       package_height,
       package_dim_unit,
       only_shoes,
       meta_create_datetime,
       meta_update_datetime
FROM dbt_edw_prod.stg.fact_order_shipment
WHERE substring(order_id, -2) = '30';
