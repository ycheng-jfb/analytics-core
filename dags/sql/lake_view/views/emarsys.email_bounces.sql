CREATE VIEW IF NOT EXISTS LAKE_VIEW.EMARSYS.EMAIL_BOUNCES AS
SELECT csm.store_group,
    s.BOUNCE_TYPE,
	s.CAMPAIGN_ID,
	s.CAMPAIGN_TYPE,
	s.CONTACT_ID,
	s.CUSTOMER_ID,
	s.DOMAIN,
	s.EMAIL_SENT_AT,
	s.EVENT_TIME,
	s.LAUNCH_ID,
	s.LOADED_AT,
	s.MESSAGE_ID,
	s.META_CREATE_DATETIME
FROM LAKE.EMARSYS.EMAIL_BOUNCES s
JOIN lake.emarsys.customer_store_mapping csm on csm.customer_id = s.customer_id;
