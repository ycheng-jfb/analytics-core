CREATE TRANSIENT TABLE if not exists staging.session_uri (
    SESSION_ID NUMBER(38,0) NOT NULL PRIMARY KEY,
    REFERER VARCHAR,
    URI VARCHAR,
    USER_AGENT VARCHAR,
    BROWSER VARCHAR,
    OS VARCHAR,
    DEVICE VARCHAR,
    IS_BOT_OLD BOOLEAN,
    IS_BOT BOOLEAN,
    CLICKID VARCHAR,
	SHAREDID VARCHAR,
	IRAD VARCHAR,
	MPID VARCHAR,
	IRMP VARCHAR,
	UTM_MEDIUM VARCHAR,
	UTM_SOURCE VARCHAR,
	UTM_CAMPAIGN VARCHAR,
	UTM_CONTENT VARCHAR,
	UTM_TERM VARCHAR,
	CCODE VARCHAR,
	PCODE VARCHAR,
	META_ORIGINAL_SESSION_ID NUMBER(38,0),
	META_ROW_HASH NUMBER(38,0),
	META_CREATE_DATETIME TIMESTAMP_LTZ(9) DEFAULT CURRENT_TIMESTAMP,
	META_UPDATE_DATETIME TIMESTAMP_LTZ(9) DEFAULT CURRENT_TIMESTAMP
);
