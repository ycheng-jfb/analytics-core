	SESSION_ID NUMBER(38,0),
	STORE_ID NUMBER(38,0),
	CUSTOMER_ID NUMBER(38,0),
    SESSION_LOCAL_DATETIME TIMESTAMP_TZ(9),
    MEDIA_SOURCE_HASH NUMBER(38,0),
    UTM_MEDIUM VARCHAR,
	UTM_SOURCE VARCHAR,
	UTM_CAMPAIGN VARCHAR,
    GATEWAY_TYPE VARCHAR,
	GATEWAY_SUB_TYPE VARCHAR,
