	SEO_VENDOR VARCHAR,
    CHANNEL VARCHAR,
    SUBCHANNEL VARCHAR,
    CLEANSED_CHANNEL_TYPE VARCHAR,
    CLEANSED_CHANNEL VARCHAR,
    CLEANSED_SUBCHANNEL VARCHAR,
    LAST_NON_DIRECT_MEDIA_SOURCE_HASH NUMBER(38,0),
    META_ORIGINAL_SESSION_ID NUMBER(38,0),
    META_ROW_HASH NUMBER(38,0),
    META_CREATE_DATETIME TIMESTAMP_LTZ(9) DEFAULT CURRENT_TIMESTAMP,
